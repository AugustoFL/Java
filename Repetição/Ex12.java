public class Ex12 {

	public static void main(String[] args) {
		
		/*12. Imprimir o quadrado dos números de 1 até 20.*/
		
	int n = 1;
	for(int i=1; i<21;i++) {
		System.out.println("O quadrado de: " + i + " é igual a: ");
		n = i*i;
		System.out.println(n + "\n");
	}
		
	}
	
}
